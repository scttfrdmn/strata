package build

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// GenerateModulefile writes an Lmod modulefile for the layer rooted at
// outputDir. The file is written to:
//
//	<outputDir>/modulefiles/<name>/<version>.lua
//
// Standard path variables (PATH, LD_LIBRARY_PATH, PKG_CONFIG_PATH, MANPATH,
// INFOPATH, CPATH) are auto-detected by inspecting the subdirectories present
// under installPrefix. Declared ModulefileEnv vars are appended as setenv
// directives. A conflict() directive is emitted for each provided capability
// name so that only one version can be active in a user session at a time.
//
// GenerateModulefile is a no-op (returns nil) for flat install layouts.
// The caller should check meta.InstallLayout before calling.
func GenerateModulefile(outputDir, installPrefix string, meta *RecipeMeta) error {
	if meta.InstallLayout == "flat" {
		return nil
	}

	// The Lmod base path uses the stable overlay mount point /strata/env.
	// Layers are mounted at /strata/env/<name>/<version>/ inside the overlay.
	name := meta.Name
	version := meta.Version

	var b strings.Builder
	b.WriteString("-- Auto-generated by strata build. Do not edit.\n")
	fmt.Fprintf(&b, "local version = %q\n", version)
	fmt.Fprintf(&b, "local base    = \"/strata/env/%s/\" .. version\n\n", name)

	if meta.Description != "" {
		fmt.Fprintf(&b, "whatis(%q)\n", meta.Description)
	}

	// Emit conflict() for each user-visible capability name.
	// Skip synthetic/ABI capability names that aren't real module names.
	if meta.UserSelectableBool() {
		skip := map[string]bool{"abi": true, "glibc": true, "mpi": true}
		for _, cap := range meta.Provides {
			if !skip[cap.Name] {
				fmt.Fprintf(&b, "conflict(%q)\n", cap.Name)
			}
		}
	}
	b.WriteString("\n")

	// Auto-detect standard subdirectories by checking what exists under
	// installPrefix (the actual temp build dir). Emit prepend_path() using
	// the stable base path expression, not the temp dir path.
	type autoVar struct {
		subdir string
		envVar string
	}
	autoVars := []autoVar{
		{"bin", "PATH"},
		{"sbin", "PATH"},
		{"lib", "LD_LIBRARY_PATH"},
		{"lib64", "LD_LIBRARY_PATH"},
		{"lib/pkgconfig", "PKG_CONFIG_PATH"},
		{"share/pkgconfig", "PKG_CONFIG_PATH"},
		{"share/man", "MANPATH"},
		{"share/info", "INFOPATH"},
		{"include", "CPATH"},
	}

	for _, av := range autoVars {
		dirPath := filepath.Join(installPrefix, av.subdir)
		if _, statErr := os.Stat(dirPath); statErr == nil {
			fmt.Fprintf(&b, "prepend_path(%q, base .. %q)\n", av.envVar, "/"+av.subdir)
		}
	}

	// Declared env vars — emitted after the auto-detected path prepends.
	if len(meta.ModulefileEnv) > 0 {
		b.WriteString("\n")
		for _, mv := range meta.ModulefileEnv {
			if mv.Path == "" {
				fmt.Fprintf(&b, "setenv(%q, base)\n", mv.Var)
			} else {
				fmt.Fprintf(&b, "setenv(%q, base .. %q)\n", mv.Var, "/"+mv.Path)
			}
		}
	}

	// Write to <outputDir>/modulefiles/<name>/<version>.lua
	mfDir := filepath.Join(outputDir, "modulefiles", name)
	if err := os.MkdirAll(mfDir, 0o755); err != nil {
		return fmt.Errorf("modulefile: creating directory: %w", err)
	}
	mfPath := filepath.Join(mfDir, version+".lua")
	return os.WriteFile(mfPath, []byte(b.String()), 0o644)
}
